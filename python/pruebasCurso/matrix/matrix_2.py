import random

def genMatrix(rows, cols):
    return [[random.randint(0, 1) for _ in range(cols)] for _ in range(rows)]

def fixMatrix(matrix):
    rows = len(matrix)
    cols = len(matrix[0])

    # Encontrar los l√≠mites del √°rea del mapa
    top, bottom, left, right = rows, 0, cols, 0

    for row in range(rows):
        for col in range(cols):
            if matrix[row][col] == '1':
                top = min(top, row)
                bottom = max(bottom, row)
                left = min(left, col)
                right = max(right, col)

    # Recortar la matriz a los l√≠mites del √°rea del mapa
    trimmed_matrix = [row[left:right + 1] for row in matrix[top:bottom + 1]]

    return trimmed_matrix

def compMatrix(original, modificada):
    errors = 0
    for row in range(len(original)):
        for col in range(len(original[0])):
            if original[row][col] != modificada[row][col]:
                errors += 1
                modificada[row][col] = "üìç"
            else:
                modificada[row][col] = "üëç"
    return errors

